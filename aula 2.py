# Para criar uma variavel, colocamos:
# o nome dela
# o simbolo de atribuição (=)
# o valor inicial dela

# Usa variavel chamada nome que recebe o meu nome
nome = ("Capita")
print("Olá, me chamo",nome)
print("O tipo de variavel é",type(nome))
print()
# Uma variavel do tipo inteiro
quantidade_alunos = 16
print(" Estamos em",quantidade_alunos,"aluno na sala")
print("O tipo ded variavel é",type(quantidade_alunos))
print()
# Uma variavel do tipo ...
salario_bruto = 1425.23
print("O salario minimo é R$",salario_bruto)
print("O tipo de variavel é",type(salario_bruto))
print()
# Uma variavel do tipo bool
is_aberta = False
print("A porta está aberta?",is_aberta)
print("O tipo de variavel é",type(is_aberta))





